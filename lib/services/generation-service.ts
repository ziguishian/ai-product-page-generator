type SectionImageAspectRatio = "1:1" | "3:4" | "9:16";

function getGenerationSettings(project: { modelSnapshot: unknown } | null) {
  const snapshot = (project?.modelSnapshot as Record<string, unknown> | null) ?? {};
  const settings = (snapshot.generationSettings as Record<string, unknown> | null) ?? {};
  const previewConfig = (snapshot.previewConfig as Record<string, unknown> | null) ?? {};

  return {
    allowSvgFallback: settings.allowSvgFallback === true,
    imageAspectRatio: previewConfig.imageAspectRatio === "3:4" ? "3:4" : "9:16",
    contentLanguage: normalizeContentLanguage(previewConfig.contentLanguage),
  } as {
    allowSvgFallback: boolean;
    imageAspectRatio: "3:4" | "9:16";
    contentLanguage: ContentLanguage;
  };
}

function getSectionAspectRatio(
  section: Pick<PageSection, "type">,
  detailAspectRatio: "3:4" | "9:16",
): SectionImageAspectRatio {
  return section.type === "HERO" ? "1:1" : detailAspectRatio;
}

function getOutputSize(aspectRatio: SectionImageAspectRatio) {
  if (aspectRatio === "1:1") {
    return "1024x1024";
  }

  return "1024x1536";
}

function isStableImageModel(modelId: string) {
  return !/(preview|experimental|beta|test)/i.test(modelId);
}

function isPreferredImageModel(modelId: string) {
  return /(banana|nano-banana|nano banana|imagen|recraft|flux|gemini|gpt-image|gpt-image-1)/i.test(modelId);
}

function isGeminiImageModel(modelId: string) {
  return /gemini.*image|nano-banana|banana/i.test(modelId);
}

function readCapabilities(model: { capabilities: unknown }) {
  return (model.capabilities as Record<string, boolean> | null) ?? {};
}

function hasImageCapability(model: { capabilities: unknown }) {
  return Boolean(readCapabilities(model).image_gen);
}

function hasTextCapability(model: { capabilities: unknown }) {
  return Boolean(readCapabilities(model).text);
}

function hasVisionCapability(model: { capabilities: unknown }) {
  return Boolean(readCapabilities(model).vision);
}

function hasRealImageGeneration(model: { capabilities: unknown }) {
  return readCapabilities(model).real_image_gen !== false;
}

function hasRealImageEdit(model: { capabilities: unknown }) {
  return readCapabilities(model).real_image_edit !== false;
}

function canGenerateRealImage(model: { capabilities: unknown }) {
  return hasImageCapability(model) && (hasRealImageGeneration(model) || isGeminiImageModel((model as { modelId?: string }).modelId ?? ""));
}

function canEditRealImage(model: { capabilities: unknown }) {
  const capabilities = readCapabilities(model);
  return (
    (Boolean(capabilities.image_edit) && capabilities.real_image_edit !== false) ||
    (Boolean(capabilities.image_gen) &&
      (capabilities.real_image_gen !== false || isGeminiImageModel((model as { modelId?: string }).modelId ?? "")))
  );
}

function buildImageModelCandidates(
  provider: ProviderContext,
  options?: {
    preferredModelId?: string | null;
    regenerate?: boolean;
    edit?: boolean;
  },
) {
  const candidatePool = provider.models.filter((item) =>
    options?.edit ? canEditRealImage(item) : canGenerateRealImage(item),
  );

  const defaultKey = options?.edit
    ? "isDefaultImageEdit"
    : options?.regenerate
      ? "isDefaultDetailImage"
      : "isDefaultHeroImage";

  const candidates = [
    options?.preferredModelId ?? null,
    candidatePool.find((item) => Boolean(item[defaultKey]))?.modelId ?? null,
    candidatePool.find((item) => item.isDefaultDetailImage)?.modelId ?? null,
    ...candidatePool
      .filter((item) => isStableImageModel(item.modelId) && isPreferredImageModel(item.modelId))
      .map((item) => item.modelId),
    ...candidatePool.filter((item) => isStableImageModel(item.modelId)).map((item) => item.modelId),
    ...candidatePool.filter((item) => isPreferredImageModel(item.modelId)).map((item) => item.modelId),
    ...candidatePool.map((item) => item.modelId),
  ].filter(Boolean) as string[];

  return [...new Set(candidates)];
}

function buildSvgModelCandidates(provider: ProviderContext) {
  const visionText = provider.models.filter((item) => hasTextCapability(item) && hasVisionCapability(item));
  const textOnly = provider.models.filter((item) => hasTextCapability(item));

  const candidates = [
    provider.models.find((item) => item.isDefaultPlanning)?.modelId ?? null,
    provider.models.find((item) => item.isDefaultAnalysis)?.modelId ?? null,
    ...visionText
      .filter((item) => /gemini|gpt-4o|gpt-5/i.test(item.modelId) && !/preview|experimental|beta|test/i.test(item.modelId))
      .map((item) => item.modelId),
    ...visionText.filter((item) => !/preview|experimental|beta|test/i.test(item.modelId)).map((item) => item.modelId),
    ...textOnly
      .filter((item) => /gemini|gpt-4o|gpt-5/i.test(item.modelId) && !/preview|experimental|beta|test/i.test(item.modelId))
      .map((item) => item.modelId),
    ...textOnly.filter((item) => !/preview|experimental|beta|test/i.test(item.modelId)).map((item) => item.modelId),
    ...textOnly.map((item) => item.modelId),
  ].filter(Boolean) as string[];

  return [...new Set(candidates)];
}

function shouldFallbackToNextImageModel(error: unknown) {
  if (!(error instanceof Error)) {
    return false;
  }

  return /404|405|429|no available endpoint|unsupported|not implemented|provider request failed|does not exist|invalid_value/i.test(
    error.message,
  );
}

function summarizeProviderImageFailure(detail: string, mode: "generate" | "edit") {
  const reasons: string[] = [];

  if (/model.+does not exist|invalid_value.+model/i.test(detail)) {
    reasons.push("代理商返回的图片模型名称无效或并不存在");
  }
  if (/no available endpoint|404/i.test(detail)) {
    reasons.push("代理商没有为这些模型开放真实图片端点");
  }
  if (/429|限流|rate limit/i.test(detail)) {
    reasons.push("代理商图片接口当前触发了限流");
  }
  if (/unknown parameter|invalid type|images\[0\]|supported values|invalid value.+size/i.test(detail)) {
    reasons.push("代理商图片接口的兼容格式与当前网关实现不一致");
  }

  if (!reasons.length) {
    reasons.push(mode === "edit" ? "图像编辑接口暂时不可用" : "图像生成接口暂时不可用");
  }

  return [...new Set(reasons)].join("；");
}

function extractJsonBlock(raw: string) {
  const direct = raw.trim();
  if (direct.startsWith("{") || direct.startsWith("[")) {
    return direct;
  }

  const fencedMatch = direct.match(/```json([\s\S]*?)```/i) || direct.match(/```([\s\S]*?)```/i);
  if (fencedMatch?.[1]) {
    return fencedMatch[1].trim();
  }

  const firstBrace = direct.indexOf("{");
  const lastBrace = direct.lastIndexOf("}");
  if (firstBrace >= 0 && lastBrace > firstBrace) {
    return direct.slice(firstBrace, lastBrace + 1);
  }

  return direct;
}

function escapeXml(value: string) {
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&apos;");
}

async function assetToDataUrl(asset: Pick<AssetRecord, "filePath" | "mimeType">) {
  const buffer = await readStorageFile(asset.filePath);
  const mimeType = asset.mimeType ?? "image/png";
  return `data:${mimeType};base64,${buffer.toString("base64")}`;
}

function pickPrimaryProductAsset(projectAssets: AssetRecord[]) {
  return (
    projectAssets.find((asset) => asset.isMain) ??
    projectAssets.find((asset) => asset.type === "MAIN") ??
    projectAssets.find((asset) => ["ANGLE", "DETAIL", "REFERENCE"].includes(asset.type)) ??
    projectAssets[0] ??
    null
  );
}

function mergeReferenceAssets(projectAssets: AssetRecord[], explicitReferenceAssets: AssetRecord[]) {
  const primaryAsset = pickPrimaryProductAsset(projectAssets);
  const merged = [primaryAsset, ...explicitReferenceAssets].filter(Boolean) as AssetRecord[];

  return merged.filter((asset, index, list) => list.findIndex((entry) => entry.id === asset.id) === index);
}

async function resolveReferenceAssets(referenceAssetIds: string[]) {
  if (!referenceAssetIds.length) {
    return [];
  }

  return prisma.productAsset.findMany({
    where: { id: { in: referenceAssetIds } },
  });
}

async function persistSectionVersion(params: {
  sectionId: string;
  imageAssetId: string;
  promptSnapshot: string;
  copySnapshot: string;
}) {
  const lastVersion = await prisma.sectionVersion.findFirst({
    where: { sectionId: params.sectionId },
    orderBy: { versionNumber: "desc" },
  });

  const versionNumber = (lastVersion?.versionNumber ?? 0) + 1;

  await prisma.sectionVersion.updateMany({
    where: { sectionId: params.sectionId },
    data: { isActive: false },
  });

  return prisma.sectionVersion.create({
    data: {
      sectionId: params.sectionId,
      versionNumber,
      promptSnapshot: { prompt: params.promptSnapshot },
      copySnapshot: { copy: params.copySnapshot },
      imageAssetId: params.imageAssetId,
      isActive: true,
    },
  });
}

async function generateWithFallback(params: {
  adapter: AdapterContext;
  candidateModels: string[];
  prompt: string;
  size: string;
  aspectRatio: SectionImageAspectRatio;
  referenceImages: string[];
  projectId: string;
  sectionId: string;
  operation: string;
}) {
  const errors: string[] = [];

  for (const model of params.candidateModels) {
    try {
      const generated = await params.adapter.generateImage({
        model,
        prompt: params.prompt,
        size: params.size,
        aspectRatio: params.aspectRatio,
        referenceImages: params.referenceImages,
        monitor: {
          projectId: params.projectId,
          sectionId: params.sectionId,
          operation: params.operation,
        },
      });

      return {
        model,
        generated,
        attemptedModels: params.candidateModels,
      };
    } catch (error) {
      const message = error instanceof Error ? error.message : "Unknown image generation error";
      errors.push(`${model}: ${message}`);

      if (!shouldFallbackToNextImageModel(error)) {
        throw error;
      }
    }
  }

  throw new Error(`所有可用图片模型都生成失败：${errors.join(" | ")}`);
}

