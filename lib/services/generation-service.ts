async function generateSvgFallback(params: {
  section: PageSection;
  adapter: AdapterContext;
  provider: ProviderContext;
  referenceAssets: AssetRecord[];
  aspectRatio: SectionImageAspectRatio;
  contentLanguage: ContentLanguage;
}) {
  const modelCandidates = buildSvgModelCandidates(params.provider);
  if (!modelCandidates.length) {
    throw new Error("当前 Provider 没有可用于 SVG 兜底预览的文本模型。");
  }

  const selectedAssets = params.referenceAssets.slice(0, 4);
  const imageInputs = await Promise.all(selectedAssets.map((asset) => assetToDataUrl(asset)));
  const layoutSpec = await generateSvgLayoutSpec({
    adapter: params.adapter,
    candidateModels: modelCandidates,
    userPrompt: buildSectionSvgLayoutPrompt(
      params.section,
      params.referenceAssets as ProductAsset[],
      params.aspectRatio,
      params.contentLanguage,
    ),
    images: imageInputs,
    projectId: params.section.projectId,
    sectionId: params.section.id,
    operation: "svg_fallback_layout",
  });

  const productImageAsset = params.referenceAssets[0] ?? null;
  const productImageDataUrl = productImageAsset ? await assetToDataUrl(productImageAsset) : null;
  const svgText = composeSectionSvg({
    section: {
      title: params.section.title,
      copy: params.section.copy,
      type: params.section.type,
    },
    layout: layoutSpec.parsed,
    productImageDataUrl,
    contentLanguage: params.contentLanguage,
  });

  return {
    svgText,
    model: layoutSpec.model,
    layout: layoutSpec.parsed,
  };
}

async function generateSectionImageInternal(
  projectId: string,
  sectionId: string,
  options?: {
    preferredModelId?: string | null;
    referenceAssetIds?: string[];
    regenerate?: boolean;
  },
) {
  const project = await prisma.project.findUnique({
    where: { id: projectId },
    include: {
      assets: { orderBy: [{ isMain: "desc" }, { sortOrder: "asc" }, { createdAt: "asc" }] },
      analysis: true,
    },
  });

  const section = await prisma.pageSection.findUnique({
    where: { id: sectionId },
  });

  if (!project) {
    throw new Error("Project not found.");
  }

  if (!section || section.projectId !== projectId) {
    throw new Error("Section not found.");
  }

  const { provider, adapter } = await getProviderAdapter();
  const generationSettings = getGenerationSettings(project);
  const sectionAspectRatio = getSectionAspectRatio(section, generationSettings.imageAspectRatio);
  const outputSize = getOutputSize(sectionAspectRatio);
  const modelCandidates = buildImageModelCandidates(provider, options);
  const selectedModel = modelCandidates[0] ?? null;
  const explicitReferenceAssets = await resolveReferenceAssets(options?.referenceAssetIds ?? []);
  const effectiveReferenceAssets = mergeReferenceAssets(project.assets as AssetRecord[], explicitReferenceAssets as AssetRecord[]);
  const referenceImages = await Promise.all(effectiveReferenceAssets.map((asset) => assetToDataUrl(asset)));
  const runningTask = await findRecentRunningTask({
    projectId,
    sectionId,
    taskType: options?.regenerate ? "REGENERATE" : "GENERATE",
    maxAgeMinutes: 20,
  });
  if (runningTask) {
    throw new Error("当前模块图仍在生成中，请等待这一轮完成后再试。");
  }

  const task = await createTask({
    projectId,
    sectionId,
    taskType: options?.regenerate ? "REGENERATE" : "GENERATE",
    inputPayload: {
      model: selectedModel,
      modelCandidates,
      referenceAssetIds: options?.referenceAssetIds ?? [],
      effectiveReferenceAssetIds: effectiveReferenceAssets.map((asset) => asset.id),
      allowSvgFallback: generationSettings.allowSvgFallback,
    },
  });

  await prisma.pageSection.update({
    where: { id: sectionId },
    data: { status: "GENERATING" },
  });

  try {
    const prompt = options?.regenerate
      ? buildRegenerationPrompt(
          section,
          effectiveReferenceAssets as ProductAsset[],
          sectionAspectRatio,
          generationSettings.contentLanguage,
        )
      : buildSectionImagePrompt(
          section,
          effectiveReferenceAssets as ProductAsset[],
          sectionAspectRatio,
          generationSettings.contentLanguage,
        );

    let imageAsset;
    let version;
    let usedModel: string;
    let generationMode: "image_api" | "svg_fallback";

    try {
      if (!selectedModel) {
        throw new Error("当前 Provider 没有探测到可用于真实图片生成的模型。");
      }

      const generation = await generateWithFallback({
        adapter,
        candidateModels: modelCandidates,
        prompt,
        size: outputSize,
        aspectRatio: sectionAspectRatio,
        referenceImages,
        projectId,
        sectionId,
        operation: options?.regenerate ? "regenerate_section_image" : "generate_section_image",
      });

      imageAsset = await saveGeneratedImage({
        projectId,
        sectionId,
        prompt,
        source: generation.generated,
        metadata: {
          mode: "image_api",
          usedModel: generation.model,
          sourceReferenceAssetIds: effectiveReferenceAssets.map((asset) => asset.id),
          primaryReferenceAssetId: effectiveReferenceAssets[0]?.id ?? null,
        },
      });

      usedModel = generation.model;
      generationMode = "image_api";
    } catch (error) {
      if (!generationSettings.allowSvgFallback) {
        const detail = error instanceof Error ? error.message : "Unknown image generation error";
        if (/monthly spending limit|spending limit|billing|quota/i.test(detail)) {
          throw new Error("当前 API Key 的图片生成额度已用尽。请前往代理商控制台提高或移除月度限额，或更换可用的 API Key。");
        }
        const summary = summarizeProviderImageFailure(detail, "generate");
        throw new Error(
          `当前 Provider 没有可用的真实图片生成端点。请前往“模型服务配置”页更换支持图片生成的 Provider，或在规划页手动开启“允许 SVG 兜底预览”。原因摘要：${summary}`,
        );
      }

      const fallback = await generateSvgFallback({
        section,
        adapter,
        provider,
        referenceAssets: effectiveReferenceAssets,
        aspectRatio: sectionAspectRatio,
        contentLanguage: generationSettings.contentLanguage,
      });

      imageAsset = await saveGeneratedImage({
        projectId,
        sectionId,
        prompt,
        source: {
          svgText: fallback.svgText,
          mimeType: "image/svg+xml",
        },
        metadata: {
          mode: "svg_fallback",
          usedModel: fallback.model,
          layout: fallback.layout,
          sourceReferenceAssetIds: effectiveReferenceAssets.map((asset) => asset.id),
          primaryReferenceAssetId: effectiveReferenceAssets[0]?.id ?? null,
          imageApiError: error instanceof Error ? error.message : "Unknown image api error",
        },
      });

      usedModel = fallback.model;
      generationMode = "svg_fallback";
    }

    version = await persistSectionVersion({
      sectionId,
      imageAssetId: imageAsset.id,
      promptSnapshot: prompt,
      copySnapshot: section.copy,
    });

    await prisma.pageSection.update({
      where: { id: sectionId },
      data: {
        status: "SUCCESS",
        currentImageAssetId: imageAsset.id,
      },
    });

    await prisma.project.update({
      where: { id: projectId },
      data: {
        status: "EDITING",
      },
    });

    await completeTask(task.id, {
      imageAssetId: imageAsset.id,
      versionId: version.id,
      usedModel,
      generationMode,
      sourceReferenceAssetIds: effectiveReferenceAssets.map((asset) => asset.id),
    });

    return { imageAsset, version, usedModel, generationMode };
  } catch (error) {
    await prisma.pageSection.update({
      where: { id: sectionId },
      data: {
        status: "FAILED",
      },
    });

    await failTask(task.id, error instanceof Error ? error.message : "Image generation failed");
    throw error;
  }
}

