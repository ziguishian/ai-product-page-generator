export async function editSectionImage(
  projectId: string,
  sectionId: string,
  options?: {
    preferredModelId?: string | null;
    referenceAssetIds?: string[];
    editMode?: "repaint" | "enhance";
  },
) {
  const project = await prisma.project.findUnique({
    where: { id: projectId },
    include: {
      assets: { orderBy: [{ isMain: "desc" }, { sortOrder: "asc" }, { createdAt: "asc" }] },
      analysis: true,
    },
  });

  const section = await prisma.pageSection.findUnique({
    where: { id: sectionId },
    include: {
      currentImageAsset: true,
    },
  });

  if (!project) {
    throw new Error("Project not found.");
  }

  if (!section || section.projectId !== projectId) {
    throw new Error("Section not found.");
  }

  if (!section.currentImageAsset) {
    throw new Error("当前模块还没有可编辑的底图，请先生成一张模块图。");
  }

  const { provider, adapter } = await getProviderAdapter();
  const generationSettings = getGenerationSettings(project);
  const sectionAspectRatio = getSectionAspectRatio(section, generationSettings.imageAspectRatio);
  const outputSize = getOutputSize(sectionAspectRatio);
  const modelCandidates = buildImageModelCandidates(provider, {
    preferredModelId: options?.preferredModelId,
    edit: true,
    regenerate: true,
  });
  const selectedModel = modelCandidates[0] ?? null;
  const explicitReferenceAssets = await resolveReferenceAssets(options?.referenceAssetIds ?? []);
  const productReferenceAssets = mergeReferenceAssets(project.assets as AssetRecord[], explicitReferenceAssets as AssetRecord[]);
  const baseImage = await assetToDataUrl(section.currentImageAsset as AssetRecord);
  const referenceImages = await Promise.all(
    productReferenceAssets
      .filter((asset) => asset.id !== section.currentImageAssetId)
      .map((asset) => assetToDataUrl(asset)),
  );
  const editMode = options?.editMode ?? "repaint";
  const runningTask = await findRecentRunningTask({
    projectId,
    sectionId,
    taskType: "REGENERATE",
    maxAgeMinutes: 20,
  });
  if (runningTask) {
    throw new Error("当前模块图仍在重绘或增强中，请等待这一轮完成后再试。");
  }

  const task = await createTask({
    projectId,
    sectionId,
    taskType: "REGENERATE",
    inputPayload: {
      mode: "edit_image",
      editMode,
      model: selectedModel,
      modelCandidates,
      baseImageAssetId: section.currentImageAssetId,
      referenceAssetIds: options?.referenceAssetIds ?? [],
      effectiveReferenceAssetIds: productReferenceAssets.map((asset) => asset.id),
      allowSvgFallback: generationSettings.allowSvgFallback,
    },
  });

  await prisma.pageSection.update({
    where: { id: sectionId },
    data: { status: "GENERATING" },
  });

  try {
    const prompt = buildImageEditPrompt(
      section,
      productReferenceAssets as ProductAsset[],
      editMode,
      sectionAspectRatio,
      generationSettings.contentLanguage,
    );

    let imageAsset;
    let version;
    let usedModel: string;
    let generationMode: "image_api" | "svg_fallback";

    try {
      if (!selectedModel) {
        throw new Error("当前 Provider 没有探测到可用于真实图片编辑的模型。");
      }

      const generation = await editWithFallback({
        adapter,
        candidateModels: modelCandidates,
        prompt,
        image: baseImage,
        size: outputSize,
        aspectRatio: sectionAspectRatio,
        referenceImages,
        projectId,
        sectionId,
        operation: editMode === "enhance" ? "enhance_section_image" : "repaint_section_image",
      });

      imageAsset = await saveGeneratedImage({
        projectId,
        sectionId,
        prompt,
        source: generation.generated,
        metadata: {
          mode: "image_api",
          usedModel: generation.model,
          editMode,
          baseImageAssetId: section.currentImageAssetId,
          sourceReferenceAssetIds: productReferenceAssets.map((asset) => asset.id),
          primaryReferenceAssetId: productReferenceAssets[0]?.id ?? null,
        },
      });

      usedModel = generation.model;
      generationMode = "image_api";
    } catch (error) {
      if (!generationSettings.allowSvgFallback) {
        const detail = error instanceof Error ? error.message : "Unknown image edit error";
        if (/monthly spending limit|spending limit|billing|quota/i.test(detail)) {
          throw new Error("当前 API Key 的图片编辑/生成额度已用尽。请前往代理商控制台提高或移除月度限额，或更换可用的 API Key。");
        }
        const summary = summarizeProviderImageFailure(detail, "edit");
        throw new Error(
          `当前 Provider 没有可用的真实图片生成或编辑端点。请前往“模型服务配置”页更换支持图片编辑的 Provider，或在规划页手动开启“允许 SVG 兜底预览”。原因摘要：${summary}`,
        );
      }

      const fallback = await generateSvgFallback({
        section,
        adapter,
        provider,
        referenceAssets: productReferenceAssets,
        aspectRatio: sectionAspectRatio,
        contentLanguage: generationSettings.contentLanguage,
      });

      imageAsset = await saveGeneratedImage({
        projectId,
        sectionId,
        prompt,
        source: {
          svgText: fallback.svgText,
          mimeType: "image/svg+xml",
        },
        metadata: {
          mode: "svg_fallback",
          usedModel: fallback.model,
          editMode,
          baseImageAssetId: section.currentImageAssetId,
          layout: fallback.layout,
          sourceReferenceAssetIds: productReferenceAssets.map((asset) => asset.id),
          primaryReferenceAssetId: productReferenceAssets[0]?.id ?? null,
          imageApiError: error instanceof Error ? error.message : "Unknown image edit api error",
        },
      });

      usedModel = fallback.model;
      generationMode = "svg_fallback";
    }

    version = await persistSectionVersion({
      sectionId,
      imageAssetId: imageAsset.id,
      promptSnapshot: prompt,
      copySnapshot: section.copy,
    });

    await prisma.pageSection.update({
      where: { id: sectionId },
      data: {
        status: "SUCCESS",
        currentImageAssetId: imageAsset.id,
      },
    });

    await prisma.project.update({
      where: { id: projectId },
      data: {
        status: "EDITING",
      },
    });

    await completeTask(task.id, {
      mode: "edit_image",
      editMode,
      imageAssetId: imageAsset.id,
      versionId: version.id,
      usedModel,
      generationMode,
      baseImageAssetId: section.currentImageAssetId,
      sourceReferenceAssetIds: productReferenceAssets.map((asset) => asset.id),
    });

    return { imageAsset, version, usedModel, generationMode, editMode };
  } catch (error) {
    await prisma.pageSection.update({
      where: { id: sectionId },
      data: {
        status: "FAILED",
      },
    });

    await failTask(task.id, error instanceof Error ? error.message : "Image edit failed");
    throw error;
  }
}

